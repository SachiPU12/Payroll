    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Connection con = null;
        ResultSet rs = null;
        PreparedStatement stmt = null;
        
        try {
            con = DriverManager.getConnection("jdbc:mysql:///abc","root","");
            String sql = "SELECT empNo,staffGrade,Name,DOB,Address,TP,Gender,basicSalary,totalLeaves,EPF FROM employee_details WHERE empNo =?";
            
            stmt=con.prepareStatement(sql);
            stmt.setString(1,jTextField1.getText());
            rs = stmt.executeQuery();
            
            if(rs.next()) { 
                String sG = rs.getString(2);
                jTextField2.setText(sG);
        
                String name = rs.getString(3);
                jTextField3.setText(name);
        
                String dob = rs.getString(4);
                jTextField9.setText(dob);
        
                String add = rs.getString(5);
                jTextArea1.setText(add);
                
                int tp = rs.getInt(6);
                String newtp = Integer.toString(tp);
                jTextField10.setText(newtp);
                
                String gen = rs.getString(7);
                    if("Male".equals(gen)){
                        jRadioButton1.setSelected(true);
                        jRadioButton2.setSelected(false);
                    }
                    else{
                        jRadioButton1.setSelected(false);
                        jRadioButton2.setSelected(true);
                    }
                
                int bs = rs.getInt(8);
                String newbs = Integer.toString(bs);
                jTextField7.setText(newbs);
                
                int tl = rs.getInt(9);
                String newtl = Integer.toString(tl);
                jTextField8.setText(newtl);
                
                String epf = rs.getString(10);
                    if("Yes".equals(epf)){
                        jRadioButton5.setSelected(true);
                        jRadioButton6.setSelected(false);
                    }
                    else{
                        jRadioButton5.setSelected(false);
                        jRadioButton6.setSelected(true);
                    }
            }
            JOptionPane.showMessageDialog(null, "Successful the searching results");
        }
        catch(SQLException e) {
            JOptionPane.showMessageDialog(null, "Unsuccessful the searching results");
        }    
    }//GEN-LAST:event_jButton1ActionPerformed

