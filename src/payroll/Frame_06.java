    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        // TODO add your handling code here:
        Connection con = null;
        PreparedStatement stmt = null;
        try{
            //create a java mysql database connection
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql:///abc","root","");

            String Gender = "";
            if(jRadioButton1.isSelected()){
                Gender = jRadioButton1.getText();
            }
            if(jRadioButton2.isSelected()){
                Gender = jRadioButton2.getText();
            }
            
            String EPF = "";
            if(jRadioButton5.isSelected()){
                EPF = jRadioButton5.getText();
            }
            if(jRadioButton6.isSelected()){
                EPF = jRadioButton6.getText();
            }
            
            //create the java mysql update preparedstatement
            stmt = con.prepareStatement("UPDATE employee_details SET staffGrade='"+jTextField2.getText()+"',Name='"+jTextField3.getText()+"',DOB='"+jTextField9.getText()+"',Address='"+jTextArea1.getText()+"',TP='"+jTextField10.getText()+"',Gender='"+Gender+"',basicSalary='"+jTextField7.getText()+"',totalLeaves='"+jTextField8.getText()+"',EPF='"+EPF+"' WHERE empNo='"+jTextField1.getText()+"'");
            
            //execute the java preparedstatement
            stmt.executeUpdate();
            
            con.close();
            stmt.close();
            JOptionPane.showMessageDialog(null, "Data is updated");
        }
        catch(ClassNotFoundException | SQLException | HeadlessException e){
            JOptionPane.showMessageDialog(null, "Data is not updated");
        }
    }//GEN-LAST:event_jButton6ActionPerformed

